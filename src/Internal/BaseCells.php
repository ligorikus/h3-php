    public static function isPentagon(int $baseCell): bool
    {
        return isset(self::IS_PENTAGON[$baseCell]);
    }

    public static function getBaseCellData(int $baseCell): ?array
    {
        return self::BASE_CELL_TABLE[$baseCell] ?? null;
    }

    public static function getNeighbor(int $baseCell, int $direction): int
    {
        return self::NEIGHBORS[$baseCell][$direction] ?? 127;
    }

    public static function faceIjkToBaseCell(int $face, int $i, int $j, int $k): int
    {
        foreach (self::BASE_CELL_TABLE as $baseCell => $data) {
            if ($data['face'] === $face && $data['i'] === $i && $data['j'] === $j && $data['k'] === $k) {
                return $baseCell;
            }
        }
        return 127;
    }

    public static function baseCellToCCWrot60(int $baseCell, int $face): int
    {
        if ($face < 0 || $face >= 20) {
            return -1;
        }

        if (!isset(C::FACE_IJK_BASECELL[$face])) {
            return -1;
        }

        $faceData = C::FACE_IJK_BASECELL[$face];

        for ($i = 0; $i < 3; $i++) {
            if (!isset($faceData[$i])) continue;
            for ($j = 0; $j < 3; $j++) {
                if (!isset($faceData[$i][$j])) continue;
                for ($k = 0; $k < 3; $k++) {
                    $entry = $faceData[$i][$j][$k] ?? null;
                    if ($entry !== null && $entry[0] === $baseCell) {
                        return $entry[1];
                    }
                }
            }
        }

        return -1;
    }
